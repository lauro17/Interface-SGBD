public class ModelColunasTabelas {

    private String Column_Name;
    private String Tipo;
    private String Nulo;
    private String ChavePrimary;

    /**
     * @return the Column_Name
     */
    public String getColumn_Name() {
        return Column_Name;
    }

    /**
     * @param Column_Name the Column_Name to set
     */
    public void setColumn_Name(String Column_Name) {
        this.Column_Name = Column_Name;
    }

    /**
     * @return the Tipo
     */
    public String getTipo() {
        return Tipo;
    }

    /**
     * @param Tipo the Tipo to set
     */
    public void setTipo(String Tipo) {
        this.Tipo = Tipo;
    }

    /**
     * @return the Nulo
     */
    public String getNulo() {
        return Nulo;
    }

    /**
     * @param Nulo the Nulo to set
     */
    public void setNulo(String Nulo) {
        this.Nulo = Nulo;
    }

    /**
     * @return the ChavePrimary
     */
    public String getChavePrimary() {
        return ChavePrimary;
    }

    /**
     * @param ChavePrimary the ChavePrimary to set
     */
    public void setChavePrimary(String ChavePrimary) {
        this.ChavePrimary = ChavePrimary;
    }

}
